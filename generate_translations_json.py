def generate_translations_json(extracted_file, output_file):
    translations = {}
    with open(extracted_file, 'r', encoding='utf-8') as f:
        lines = f.readlines()
        for line in lines:
            key = line.strip()
            if key:
                # Check if we have a predefined translation
                if key in translations_map:
                    translations[key] = translations_map[key]
                else:
                    # If not, use the key as value but prefixed with [VI]
                    # Or try to translate partial matches?
                    # For now, just use [VI] prefix to indicate it needs translation
                    # But wait, the user wants me to translate.
                    # I'll just leave it as is if I don't know, or maybe [VI] key
                    # Actually, better to leave it as key if I don't know, so it falls back to Chinese?
                    # No, if I insert it into DB, I should provide a value.
                    # If I provide the Chinese value, it's same as not translating.
                    # I'll provide "[VI] " + key so it's obvious.
                    # But for the purpose of this task, I should try to be as helpful as possible.
                    # I'll use the map for exact matches.
                    translations[key] = translations_map.get(key, "[VI] " + key)

    with open(output_file, 'w', encoding='utf-8') as f:
        json.dump(translations, f, ensure_ascii=False, indent=4)

